#include "py.h"
/**
 * add - adds a and b
 * @a: the first value
 * @b: the second value
 *
 * Return: sum
*/
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - adds a and b
 * @a: the first value
 * @b: the second value
 *
 * Return: difference
*/
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - adds a and b
 * @a: the first value
 * @b: the second value
 *
 * Return: multiplication
*/
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - adds a and b
 * @a: the first value
 * @b: the second value
 *
 * Return: division
*/
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - adds a and b
 * @a: the first value
 * @b: the second value
 *
 * Return: modulus
*/
int mod(int a, int b)
{
	return (a % b);
}
